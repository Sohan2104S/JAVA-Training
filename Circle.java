class Circle
{
	public static void main(String[]args)
	{
		float radius=5.5f;
		float pi=22/7;
		
		float perimeter=2*pi*radius;
		float area=pi*radius*radius;

		System.out.println("Area of circle of radius 5.5 is : "+area );
		System.out.println("Perimeter of Cicle is of radius 5.5 is " + perimeter);
	}
}