class AreaOfRectangle
{
	public static void main(String[]args)
	{
		float height=4.5f;
		float width=7.9f;

		float area=height*width;
		float perimeter=2*(height+width);

		System.out.println("Area of rectangle of height 4.5 and width 7.9  is "+ area);
		System.out.println("Perimeter of rectangle of height 4.5 and width 7.9  is  "+ perimeter);
	}
}